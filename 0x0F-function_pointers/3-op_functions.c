/**
 * op_add - add two numbers
 * @a: first number
 * @b: second number
 * Return: result of a plus b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract a number from another
 * @a: first number
 * @b: second number
 * Return: result of b minus a
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two numbers
 * @a: first number
 * @b: second number
 * Return: result of a multiply b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide a number by another
 * @a: first number
 * @b: second number
 * Return: result of a divided by b
 */

int op_div(int a, int b)
{

	return (a / b);
}

/**
 * op_mod - perform modulo operation on two numbers
 * @a: first number
 * @b: second number
 * Return: result of a module b
 */

int op_mod(int a, int b)
{

	return (a % b);
}
